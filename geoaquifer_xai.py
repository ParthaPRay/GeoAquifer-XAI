# Cell 5 — Read all sheets and standardize columns

def clean_sample_type(x):
    if pd.isna(x):
        return np.nan
    x = str(x).strip().lower()
    if "identified" in x and "hot" in x:
        return "Identified Hot Spot"
    elif "grid" in x:
        return "Grid Sample"
    return str(x)

def read_and_standardize_excel(excel_path, sheet_config):
    all_frames = []

    for sheet, cfg in sheet_config.items():
        df = pd.read_excel(excel_path, sheet_name=sheet)

        df = df.rename(columns={
            "Hot Spot No.": "hotspot_no_raw",
            "Type of Sample in grid (Identified hot spot/Grid Sample)": "sample_type_raw",
            "State/UT": "state_ut",
            "District": "district",
            "Block/Taluka": "block_taluka",
            "Hot Spot Location/Sample Location around hot Spot": "location",
            "Longitude (DD)": "longitude",
            "Latitude (DD)": "latitude",
            "Source of Sample": "source",
            cfg["value_col"]: "value"
        })

        df["contaminant"] = cfg["contaminant"]
        df["unit"] = cfg["unit"]
        df["bis_threshold"] = cfg["threshold"]
        df["threshold_label"] = cfg["threshold_label"]
        df["sheet_name"] = sheet

        df["sample_type"] = df["sample_type_raw"].apply(clean_sample_type)

        # Hotspot number appears only in hotspot center rows.
        # Forward fill to assign surrounding grid samples to the nearest listed hotspot group.
        df["hotspot_no"] = df["hotspot_no_raw"].ffill()

        # Clean numeric fields
        df["longitude"] = pd.to_numeric(df["longitude"], errors="coerce")
        df["latitude"] = pd.to_numeric(df["latitude"], errors="coerce")
        df["value"] = pd.to_numeric(df["value"], errors="coerce")

        # Standard text columns
        for col in ["state_ut", "district", "block_taluka", "location", "source"]:
            df[col] = df[col].astype(str).str.strip()

        df["exceeds_bis"] = df["value"] > df["bis_threshold"]
        df["exceedance_ratio"] = df["value"] / df["bis_threshold"]

        all_frames.append(df)

    final_df = pd.concat(all_frames, ignore_index=True)

    return final_df

