def plot_single_contaminant_map(
    contaminant_name,
    gdf_points,
    india_states,
    aquifer_layer=None,
    value_column="log10_value",
    output_dir=FIGDIR
):
    sub = gdf_points[gdf_points["contaminant"] == contaminant_name].copy()

    if sub.empty:
        print("No data for", contaminant_name)
        return

    cmap = CONTAMINANT_CMAPS.get(contaminant_name, "viridis")

    fig, ax = plt.subplots(figsize=(8.5, 10))

    if aquifer_layer is not None:
        aquifer_layer.plot(
            ax=ax,
            color="lightgrey",
            edgecolor="none",
            alpha=0.20,
            zorder=1
        )

    india_states.plot(
        ax=ax,
        facecolor="white",
        edgecolor="black",
        linewidth=0.35,
        alpha=0.95,
        zorder=2
    )

    grid = sub[sub["sample_type"] == "Grid Sample"]
    hot = sub[sub["sample_type"] == "Identified Hot Spot"]

    # Smaller markers for arsenic to avoid border spillover
    if contaminant_name == "Arsenic":
        grid_size = 14
        hot_size = 70
    else:
        grid_size = 28
        hot_size = 130

    grid.plot(
        ax=ax,
        column=value_column,
        cmap=cmap,
        markersize=grid_size,
        marker="o",
        alpha=0.85,
        edgecolor="black",
        linewidth=0.20,
        legend=False,
        zorder=3
    )

    hot.plot(
        ax=ax,
        column=value_column,
        cmap=cmap,
        markersize=hot_size,
        marker="*",
        alpha=1.0,
        edgecolor="black",
        linewidth=0.45,
        legend=False,
        zorder=4
    )

    norm = mpl.colors.Normalize(
        vmin=sub[value_column].min(),
        vmax=sub[value_column].max()
    )

    sm = mpl.cm.ScalarMappable(cmap=cmap, norm=norm)
    sm._A = []

    cbar = fig.colorbar(sm, ax=ax, shrink=0.70, pad=0.02)

    if value_column == "log10_value":
        cbar.set_label(f"log10 concentration ({sub['unit'].iloc[0]})")
    elif value_column == "log10_exceedance_ratio":
        cbar.set_label("log10 concentration / BIS limit")
    else:
        cbar.set_label(f"Concentration ({sub['unit'].iloc[0]})")

    grid_handle = mpl.lines.Line2D(
        [], [], color="black", marker="o", linestyle="None",
        markersize=5, label="Grid sample"
    )

    hot_handle = mpl.lines.Line2D(
        [], [], color="black", marker="*", linestyle="None",
        markersize=9, label="Identified hotspot"
    )

    ax.legend(
        handles=[hot_handle, grid_handle],
        loc="lower right",
        frameon=True,
        framealpha=0.9
    )

    ax.set_title(
        f"{contaminant_name} Groundwater Contamination Hotspots, India\n"
        f"Pre-monsoon 2024 grid sampling; {sub['threshold_label'].iloc[0]}",
        fontsize=12,
        fontweight="bold"
    )

    # Force India-only map extent
    minx, miny, maxx, maxy = india_states.total_bounds
    ax.set_xlim(minx, maxx)
    ax.set_ylim(miny, maxy)

    ax.set_axis_off()
    add_north_arrow(ax)

    plt.tight_layout()

    safe_name = contaminant_name.replace("/", "_").replace(" ", "_")
    png_path = os.path.join(output_dir, f"India_{safe_name}_{value_column}_cleaned.png")
    pdf_path = os.path.join(output_dir, f"India_{safe_name}_{value_column}_cleaned.pdf")

    plt.savefig(png_path, dpi=600, bbox_inches="tight")
    plt.savefig(pdf_path, bbox_inches="tight")
    plt.show()

    print("Saved:", png_path)
    print("Saved:", pdf_path)

