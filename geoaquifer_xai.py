# Cell 26 — Haversine distance function

#This calculates distance between hotspot centre and surrounding grid samples.

def haversine_km(lon1, lat1, lon2, lat2):
    R = 6371.0088
    lon1, lat1, lon2, lat2 = map(np.radians, [lon1, lat1, lon2, lat2])
    dlon = lon2 - lon1
    dlat = lat2 - lat1

    a = np.sin(dlat / 2.0) ** 2 + np.cos(lat1) * np.cos(lat2) * np.sin(dlon / 2.0) ** 2
    c = 2 * np.arcsin(np.sqrt(a))

    return R * c

# Cell 27 — Assign hotspot centre coordinates to grid samples

def attach_hotspot_centres(gdf_in):
    rows = []

    for contaminant, cdf in gdf_in.groupby("contaminant"):
        for hotspot_no, hdf in cdf.groupby("hotspot_no"):
            centre = hdf[hdf["sample_type"] == "Identified Hot Spot"]

            if centre.empty:
                continue

            # If more than one centre row exists, take first
            centre_row = centre.iloc[0]

            temp = hdf.copy()
            temp["hotspot_latitude"] = centre_row["latitude"]
            temp["hotspot_longitude"] = centre_row["longitude"]
            temp["hotspot_value"] = centre_row["value"]
            temp["hotspot_location"] = centre_row["location"]

            temp["distance_from_hotspot_km"] = haversine_km(
                temp["hotspot_longitude"],
                temp["hotspot_latitude"],
                temp["longitude"],
                temp["latitude"]
            )

            temp["relative_to_hotspot"] = temp["value"] / centre_row["value"]

            rows.append(temp)

    out = pd.concat(rows, ignore_index=True)

    out_gdf = gpd.GeoDataFrame(
        out,
        geometry=gpd.points_from_xy(out["longitude"], out["latitude"]),
        crs="EPSG:4326"
    )

    return out_gdf

