# CELL 35 — Strictly clipped IDW interpolation
# =========================

'''
| Resolution | Meaning    | Output              | Use case                                  |
| ---------: | ---------- | ------------------- | ----------------------------------------- |
|    `50000` | 50 km grid | coarse, fast        | quick national draft                      |
|    `30000` | 30 km grid | balanced            | recommended for India-wide map            |
|    `20000` | 20 km grid | smoother            | good for journal figure                   |
|    `10000` | 10 km grid | detailed but slower | state/regional maps                       |
|     `5000` | 5 km grid  | very dense, slow    | not recommended for national hotspot data |

'''

from scipy.spatial import cKDTree

def idw_interpolation_strict(
    points_gdf,
    value_col,
    boundary_gdf,
    resolution=30000,
    power=2,
    k_neighbors=8
):
    """
    Strict IDW interpolation clipped to India boundary.

    resolution:
        Grid spacing in projected units.
        Under EPSG:3857, this is approximately metres.
        30000 means 30 km grid spacing.

    power:
        IDW power parameter.
        Higher power gives more local influence.
    """

    pts = points_gdf.to_crs(PLOT_CRS).copy()
    boundary = boundary_gdf.to_crs(PLOT_CRS).copy()

    boundary["geometry"] = boundary["geometry"].apply(
        lambda geom: make_valid(geom) if geom is not None else None
    )

    boundary = boundary[~boundary.geometry.isna()].copy()

    # Dissolve to one India polygon
    boundary_union = boundary.dissolve()

    minx, miny, maxx, maxy = boundary_union.total_bounds

    xs = np.arange(minx, maxx, resolution)
    ys = np.arange(miny, maxy, resolution)

    grid_x, grid_y = np.meshgrid(xs, ys)

    grid_df = pd.DataFrame({
        "x": grid_x.ravel(),
        "y": grid_y.ravel()
    })

    grid_gdf = gpd.GeoDataFrame(
        grid_df,
        geometry=gpd.points_from_xy(grid_df["x"], grid_df["y"]),
        crs=PLOT_CRS
    )

    # Strict spatial clipping before interpolation
    grid_gdf = gpd.sjoin(
        grid_gdf,
        boundary_union[["geometry"]],
        how="inner",
        predicate="within"
    ).drop(columns=["index_right"])

    print("Interpolation grid points inside India:", len(grid_gdf))

    coords = np.vstack([pts.geometry.x, pts.geometry.y]).T
    values = pts[value_col].values

    tree = cKDTree(coords)

    k = min(k_neighbors, len(coords))

    distances, indices = tree.query(
        np.vstack([grid_gdf.geometry.x, grid_gdf.geometry.y]).T,
        k=k
    )

    if k == 1:
        distances = distances.reshape(-1, 1)
        indices = indices.reshape(-1, 1)

    distances = np.where(distances == 0, 1e-12, distances)

    weights = 1 / distances ** power

    interpolated = np.sum(weights * values[indices], axis=1) / np.sum(weights, axis=1)

    grid_gdf["idw_value"] = interpolated

    return grid_gdf

