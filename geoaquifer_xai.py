def get_model_probability(model_pipe, X_data):
    """
    Return probability-like score for binary classification.
    """
    clf = model_pipe.named_steps["model"]

    if hasattr(clf, "predict_proba"):
        return model_pipe.predict_proba(X_data)[:, 1]

    score = model_pipe.decision_function(X_data)

    if score.max() == score.min():
        return np.repeat(0.5, len(score))

    return (score - score.min()) / (score.max() - score.min())

# ---------------------------------------------------------
# 8. Threshold tuning helper
# ---------------------------------------------------------

def tune_threshold(y_true, y_prob, metric="balanced_accuracy"):
    """
    Tune probability threshold using validation data.

    metric options:
    - balanced_accuracy
    - accuracy
    - f1_score
    """
    thresholds = np.arange(0.25, 0.751, 0.005)

    records = []

    for thr in thresholds:
        y_pred = (y_prob >= thr).astype(int)

        precision, recall, f1, _ = precision_recall_fscore_support(
            y_true,
            y_pred,
            average="binary",
            zero_division=0
        )

        records.append({
            "threshold": float(thr),
            "accuracy": accuracy_score(y_true, y_pred),
            "balanced_accuracy": balanced_accuracy_score(y_true, y_pred),
            "precision": precision,
            "recall": recall,
            "f1_score": f1
        })

    threshold_df = pd.DataFrame(records)

    if metric == "accuracy":
        sort_cols = ["accuracy", "balanced_accuracy", "f1_score"]
    elif metric == "f1_score":
        sort_cols = ["f1_score", "balanced_accuracy", "accuracy"]
    else:
        sort_cols = ["balanced_accuracy", "accuracy", "f1_score"]

    best_row = threshold_df.sort_values(
        sort_cols,
        ascending=False
    ).iloc[0]

    return float(best_row["threshold"]), threshold_df

